def auto_delete(msg, delay=20):
    """Automatically deletes a message after N seconds."""
    if not msg: return
    def do_delete():
        import time
        time.sleep(delay)
        try: bot.delete_message(msg.chat.id, msg.message_id)
        except: pass
    import threading
    threading.Thread(target=do_delete).start()

